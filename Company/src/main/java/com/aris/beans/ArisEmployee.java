package com.aris.beans;

public class ArisEmployee {

	private int PAT_ID;
	private String PAT_NAME;
	private String PAT_ADDRESS;
	private int PAT_CONTACTNO;

	public int getPAT_ID() {
		return PAT_ID;
	}
	public void setPAT_ID(int pAT_ID) {
		PAT_ID = pAT_ID;
	}
	public String getPAT_NAME() {
		return PAT_NAME;
	}
	public void setPAT_NAME(String pAT_NAME) {
		PAT_NAME = pAT_NAME;
	}
	public String getPAT_ADDRESS() {
		return PAT_ADDRESS;
	}
	public void setPAT_ADDRESS(String pAT_ADDRESS) {
		PAT_ADDRESS = pAT_ADDRESS;
	}
	public int getPAT_CONTACTNO() {
		return PAT_CONTACTNO;
	}
	public void setPAT_CONTACTNO(int pAT_CONTACTNO) {
		PAT_CONTACTNO = pAT_CONTACTNO;
	}	
}
